/** Deterministic hue 0-359 from a slug so every tool always has the same vibrant color. */
export function slugToHue(slug: string): number {
    let h = 5381;
    for (let i = 0; i < slug.length; i++) h = ((h * 33) ^ slug.charCodeAt(i)) >>> 0;
    return h % 360;
}

export function slugToColor(slug: string): string {
    return `hsl(${slugToHue(slug)}, 80%, 52%)`;
}

export function buildFaviconSvg(slug: string, bgColor?: string): string {
    const bg = (!bgColor || bgColor === "#000000") ? slugToColor(slug) : bgColor;
    const config = TOOL_ICONS[slug];
    if (!config) {
        const letter = (slug[0] ?? "?").toUpperCase();
        return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><rect width="24" height="24" rx="5" fill="${bg}"/><text x="12" y="12" text-anchor="middle" dominant-baseline="central" font-family="system-ui,-apple-system,sans-serif" font-weight="900" font-size="16" fill="white">${letter}</text></svg>`;
    }
    const pathElements = config.paths
        .map((d) => `<path stroke-linecap="round" stroke-linejoin="round" d="${d}" />`)
        .join("");
    return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><rect width="24" height="24" rx="5" fill="${bg}"/><g transform="translate(3,3) scale(0.75)" fill="none" stroke="white" stroke-width="1.8">${pathElements}</g></svg>`;
}
